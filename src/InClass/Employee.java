package InClass;

public class Employee {
    private String name;
    private double salary;

    public Employee(String aName, double aSalary) {
        this.name = aName;
        this.salary = aSalary;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getSalary() {
        return salary;
    }

    public void setSalary(double salary) {
        this.salary = salary;
    }




}
